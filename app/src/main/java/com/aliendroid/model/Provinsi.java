package com.aliendroid.model;

public class Provinsi {

    public int FID;
    public String Kode_Provi;
    public String Provinsi;
    public String Kasus_Posi;
    public String Kasus_Semb;
    public String Kasus_Meni;



    public int getFID() {
        return FID;
    }

    public String getKode_Provi() {
        return Kode_Provi;
    }

    public String getProvinsi(){
        return Provinsi;
    }

    public String getKasus_Posi(){
        return Kasus_Posi;
    }

    public String getKasus_Semb(){
        return Kasus_Semb;
    }

    public String getKasus_Meni(){
        return Kasus_Meni;
    }

}
